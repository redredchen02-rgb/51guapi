const NON_GOSSIP_PATTERNS = [
	/第[一二三四五六七八九十0-9]+届/,
	/第[一二三四五六七八九十0-9]+轮/,
	/[0-9]+比[0-9]+/,
	/[0-9]+-[0-9]+战胜/,
	/击败.*夺冠/,
	/晋级.*决赛/,
	/创.*新高/,
	/创.*新低/,
];

/**
 * 判断一个热搜词是否属于娱乐八卦/吃瓜范畴。
 * 使用基于规则的黑名单过滤掉政治、宏观经济、体育赛事结果、灾害天气、军事航天、教育考试等非八卦内容。
 */
export function isGossipOrEntertainment(keyword: string): boolean {
	if (!keyword) return false;

	const normalized = keyword.toLowerCase();

	// 1. 检查关键字黑名单
	for (const kw of NON_GOSSIP_KEYWORDS) {
		if (normalized.includes(kw.toLowerCase())) {
			return false;
		}
	}

	// 2. 检查正则匹配
	for (const pattern of NON_GOSSIP_PATTERNS) {
		if (pattern.test(normalized)) {
			return false;
		}
	}

	return true;
}
